package main.java.com.danyatheworst.currency;

public class CurrencyResponse {
    private int id;
    private String code;
    private String name;
    private String sign;

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
}
